# برنامه‌ای بنویسید که از کاربر یک عدد بگیرد و مجموع ارقام آن را محاسبه کرده در خروجی چاپ کند

def sum_digits(number):
  """
  این تابع ارقام یک عدد را با هم جمع می‌بندد.
  """
  num_str = str(number)  # اول عدد رو به رشته تبدیل می‌کنیم تا بتونیم به تک تک ارقامش دسترسی داشته باشیم. 🧐
  digit_sum = 0  # یه متغیر برای جمع ارقام درست می‌کنیم و اولش صفره. 💯
  for digit in num_str:
    digit_sum += int(digit)  # هر رقم رو به عدد تبدیل می‌کنیم و به جمع اضافه می‌کنیم. ✨
  return digit_sum

# از کاربر عدد رو می‌گیریم! 🤩
try:
  user_input = input("یه عدد رو بهم بگو لطفاً! 🔢: ")
  user_number = int(user_input) # مطمئن می‌شیم که ورودی کاربر عدد باشه. 🤓

  # حاصل جمع ارقام رو حساب می‌کنیم و نشونش می‌دیم! 🎉
  result = sum_digits(user_number)
  print(f"حاصل جمع ارقام عدد {user_number} برابر است با: {result} 🥳")

except ValueError:
  print("اوه اوه! 😥 فکر کنم یه چیزی اشتباه وارد کردی. لطفاً فقط عدد وارد کن! 🙏")

